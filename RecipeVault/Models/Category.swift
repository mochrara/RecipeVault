enum Category: String {
    /// Breakfast category
    case breakfast = "Breakfast"
    /// Lunch category
    case lunch = "Lunch"
    /// Dinner category
    case dinner = "Dinner"
    /// Snacks category
    case snacks = "Snacks"
    /// Desserts category
    case desserts = "Desserts"
    /// Beverages category
    case beverages = "Beverages"
    /// Appetizers category
    case appetizers = "Appetisers"
    /// Salads category
    case salads = "Salads"
    /// Soups category
    case soups = "Soups"
}
